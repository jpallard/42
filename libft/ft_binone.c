#include "ft_printf.h"

void	ft_binone(t_struct *t, t_conv *p, wint_t n)
{
	char c;

	p->exe = 1;
	c = (char)n;
	write(1, &c, 1);
	t->c = t->c + ft_charlen(n, t);
}
