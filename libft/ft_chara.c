#include "ft_printf.h"

int		ft_chara(const char *ft, int i, t_struct *t)
{
	while (ft[i] != '%' && ft[i])
	{
		t->c++;
		ft_putchar(ft[i]);
		i++;
	}
	t->per = i;
	return (i);
}
