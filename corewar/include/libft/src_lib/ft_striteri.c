#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;
	int j;

	i = 0;
	j = 0;
	if (s && f)
	{
		while (s[i])
		{
			(*f)(j, &s[i]);
			i++;
			j++;
		}
	}
}
